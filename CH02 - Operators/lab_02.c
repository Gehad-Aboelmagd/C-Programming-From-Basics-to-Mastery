#include <stdio.h>

int main(void){

    int x = 7;   // 0000 0000 0000 0000 0000 0000 0000 0111  or  0x 00 00 00 07 
    int y = 4;   // 0000 0000 0000 0000 0000 0000 0000 0100  or  0x 00 00 00 04

    // x & y =      0000 0000 0000 0000 0000 0000 0000 0100  or 4
    printf("x & y = %d\n", x&y);

    // x | y =      0000 0000 0000 0000 0000 0000 0000 0111  or 7
    printf("x | y = %d\n", x|y);

    // x ^ y =      0000 0000 0000 0000 0000 0000 0000 0011  or 3
    printf("x ^ y = %d\n", x^y);

    //  ~x   =      1111 1111 1111 1111 1111 1111 1111 1000  or -8
    printf("~ x = %d\n", ~x);

    // x >> 1 =     0000 0000 0000 0000 0000 0000 0000 0011.1 
    // but the fraction part is truncated so, the result is only 3 not 3.5 
    // x >> 1 =     0000 0000 0000 0000 0000 0000 0000 0011
    printf("x >> 1 = %d\n", x>>1);

    // y << 2 =     0000 0000 0000 0000 0000 0000 0001 0000  or 4*(2^2)=16
    printf("y << 2 = %d\n", y<<2);
}